class Solution {
    public int concatenatedBinary(int n) {
        final int MOD = 1_000_000_007;
        long ans = 0;
        int bitLength = 0;
        for (int i = 1; i <= n; i++) {
            // Increase bit length when i is a power of 2
            if ((i & (i - 1)) == 0) {
                bitLength++;
            }
            ans = ((ans << bitLength) % MOD + i) % MOD;
        }
        return (int) ans;
    }
}
